<h1>Global Chat Rules</h1>
<p>
    These rules are not intended as a comprehensive list of permitted and forbidden behavior, but as a general guideline on acceptable conduct. You are expected
    to use your own common sense judgement, and moderators may act outside of the rules at their discretion within reason.
</p>
<p>
    Disputes regarding interpretations of the rules or moderator actions may be sent to modmail in the
    <A to="https://discord.gg/FG2wpbywSx" external>TCN Hub</A> by DMing <UserMention id="1010190605890109497" /> after joining the server.
</p>
<p>Inappropriate behavior in DMs or in TCN servers outside of global chat may also result in penalties applied in global chat.</p>
<br />
<Linkable short e="h3" id="be-nice" class="row gap-1"><code>#1</code> Be Nice</Linkable>
<ul class="space">
    <li>No harassment, discrimination, personal attacks, hateful language, use of slurs, brigading, witch-hunting, etc.</li>
    <li>
        In case of other users being disrespectful, disengage from the situation and alert mods if needed; insulting the user back will result in both parties
        being punished accordingly to their behavior.
    </li>
    <li>Being nice extends to all parties &mdash; other members, moderators, users not in the global chat, non-TCN servers, etc.</li>
</ul>
<Linkable short e="h3" id="no-nsfw" class="row gap-1"><code>#2</code> No NSFW/NSFL</Linkable>
<ul class="space">
    <li>Most connected servers are strict on NSFW, so even if the server from which you are chatting is lenient on this, it still isn't allowed.</li>
    <li>NSFW/NSFL content includes everything - media, text (including usernames), profile avatars, etc. both explicitly and implicitly.</li>
</ul>
<Linkable short e="h3" id="no-spoilers" class="row gap-1"><code>#3</code> No Leaks/Unmarked Spoilers</Linkable>
<ul class="space">
    <li>Leaks are not allowed in global channels and should be kept to channels in your server that permit leaks.</li>
    <li>This rule applies to both Genshin Impact and any other games / media.</li>
    <li>Mark spoilers, both for Genshin Impact and otherwise (within reason).</li>
</ul>
<Linkable short e="h3" id="no-drama" class="row gap-1"><code>#4</code> No Drama</Linkable>
<ul class="space">
    <li>
        Do not complain about your ban/mute in another server - if you have an issue with a TCN server's actions, you can contact the TCN via modmail as
        detailed at the top of this document.
    </li>
    <li>
        Political and religious conversations are not banned outright, but as they can rapidly start drama, we discourage them as they may get out of your
        control and result in the conversation being shut down and/or punishments.
    </li>
    <li>Personal drama and server drama, whether or not from within the TCN, is not to be discussed in global chat.</li>
    <li>Do not complain about moderator actions here; contact the TCN via modmail if you wish to dispute actions or punishments.</li>
</ul>
<Linkable short e="h3" id="no-spam" class="row gap-1"><code>#5</code> No Spam</Linkable>
<ul class="space">
    <li>Do not flood chat; be considerate of others' use of the channel.</li>
    <li>Do not jump around servers just for the sake of it as that is disruptive and causes your consecutive messages to not group together.</li>
</ul>
<Linkable short e="h3" id="no-advertisement" class="row gap-1"><code>#6</code> No Advertisement</Linkable>
<ul class="space">
    <li>Do not advertise - this includes self-promotion, server advertisement, etc.</li>
</ul>
<Linkable short e="h3" id="language" class="row gap-1"><code>#7</code> Language &mdash; English</Linkable>
<ul class="space">
    <li>For purposes of moderation, please keep conversations to English only.</li>
    <li>Words, well-known phrases, short snippets, and memes in other languages are permitted.</li>
</ul>
<Linkable short e="h3" id="listen-to-staff" class="row gap-1"><code>#8</code> Listen to Staff</Linkable>
<ul class="space">
    <li>These rules are guidelines and not intended to be exhaustive.</li>
    <li>Moderators are entrusted to make the final call on situations.</li>
    <li>If you believe a moderator's actions were not in good faith, counterproductive, or otherwise a mistake, contact the TCN via modmail.</li>
</ul>
