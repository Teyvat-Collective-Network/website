<h1>Hub Communication Standards</h1>
<p>This document concerns the standards of communication for announcements to the TCN Hub in <Mention type="news">announcements</Mention>.</p>
<p>Pings may be for <Mention>Announcements</Mention> or <Mention>everyone</Mention>, depending on the importance and impact.</p>
<ul class="space">
    <li>
        <b>Induction of new servers</b> &mdash; When new servers are added, an announcement should be sent informing the Hub of the new member server.
    </li>
    <li>
        <b>Departure of former partners</b> &mdash; When servers leave the network, they may optionally provide a reason to be given publicly, though this
        reason may be excluded if the council believes it is inaccurate or slanderous. However, no reason may be given that was not approved by the departing
        server; instead, it will only be stated that it was voluntary if so and no further information will be given otherwise. <b>No ping</b>
        will be made for these announcements.
    </li>
    <li>
        <b>Election results</b> &mdash; The results of elections will be announced, even if no change is made to the observer team, to keep everyone updated on the
        representatives of the network.
    </li>
    <li>
        <b>Early departure of observers</b> &mdash; When observers resign from the team, the same procedure is followed as for the departure of former partners.
        However, a ping <b>will</b> be given.
    </li>
    <li>
        <b>Significant restructuring / changes</b> &mdash; if the Hub or the entire network is restructured or changes are made to the rules or network in a way
        meaningful to the general public, an announcement will be made.
    </li>
</ul>
